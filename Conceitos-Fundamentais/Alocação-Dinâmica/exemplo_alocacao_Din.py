# Exemplo de Alocação Dinâmica com Listas em Python

# Em Python, as listas são alocadas dinamicamente, ou seja, podemos adicionar ou remover elementos a qualquer momento.

# Inicializando uma lista vazia
lista = []

# Adicionando elementos dinamicamente
for i in range(10):
    lista.append(i)  # Alocação dinâmica: o tamanho da lista pode crescer durante a execução

# Imprimindo os valores
print("Lista de alocação dinâmica:", lista)



